package com.mobmonkey.mobmonkey.utils;

/**
 * @author Dezapp, LLC
 *
 */
public class MMResultsLocation {
	private String locName;
	private String locDist;
	private String locAddr;
	
	/**
	 * @return the locName
	 */
	public String getLocName() {
		return locName;
	}
	
	/**
	 * @param locName the locName to set
	 */
	public void setLocName(String locName) {
		this.locName = locName;
	}
	
	/**
	 * @return the locDist
	 */
	public String getLocDist() {
		return locDist;
	}
	
	/**
	 * @param locDist the locDist to set
	 */
	public void setLocDist(String locDist) {
		this.locDist = locDist;
	}
	
	/**
	 * @return the locAddr
	 */
	public String getLocAddr() {
		return locAddr;
	}
	
	/**
	 * @param locAddr the locAddr to set
	 */
	public void setLocAddr(String locAddr) {
		this.locAddr = locAddr;
	}
}
