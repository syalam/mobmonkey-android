public class MMLocationDetailsItem {
	private int locationDetailIconId;
	private String locationDetail;
	
	/**
	 * 
	 * @return the locationDetailIconId
	 */
	public int getLocationDetailIconId() {
		return locationDetailIconId;
	}
	
	/**
	 * 
	 * @param locationDetailIconId the locationDetailIconId to set
	 */
	public void setLocationDetailIconId(int locationDetailIconId) {
		this.locationDetailIconId = locationDetailIconId;
	}
	
	/**
	 * 
	 * @return the locationDetail
	 */
	public String getLocationDetail() {
		return locationDetail;
	}
	
	/**
	 * 
	 * @param locationDetail the locationDetail to set
	 */
	public void setLocationDetail(String locationDetail) {
		this.locationDetail = locationDetail;
	}
	

}
